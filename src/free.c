#include "minishell.h"

void	ft_free_tab(char **tab)
{
	int	i;

	i = 0;
	while (tab[i])
	{
		free(tab[i]);
		++i;
	}
	free(tab);
}

void	ft_cleanup(t_env *env)
{
	ft_free_tab(env->envp);
	ft_free_tab(env->path);
}
